package proje;

public class Destination {
	
	private String Capital1;
	private String Capital2;
	
	public Destination(String capital1, String capital2) {
		Capital1 = capital1;
		Capital2 = capital2;
	}

	public String getCapital1() {
		return Capital1;
	}

	public void setCapital1(String capital1) {
		Capital1 = capital1;
	}

	public String getCapital2() {
		return Capital2;
	}

	public void setCapital2(String capital2) {
		Capital2 = capital2;
	}
	
}
